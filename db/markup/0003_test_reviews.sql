-- Recompute denormalized rating columns for any provider that has reviews.
UPDATE p
SET p.RatingAverage = r.AvgRating,
    p.RatingCount   = r.Cnt,
    p.UpdatedAtUtc  = SYSUTCDATETIME()
FROM dbo.Providers p
JOIN (
    SELECT ProviderId, CAST(AVG(CAST(Rating AS DECIMAL(3,2))) AS DECIMAL(3,2)) AS AvgRating, COUNT(*) AS Cnt
    FROM dbo.Reviews
    GROUP BY ProviderId
) r ON r.ProviderId = p.Id;
